# Crear una función que calcule la longitud de una circunferencia:
print("Función de la longitud de una cicunferencia:")

def longitudcircunferencia ():
    radio = int(input("Introduzca el valor del radio: "))
    longitud = 2 * 3.14159 * radio
    print("La longitud de la circunferencia es:", round(longitud, 4))

longitudcircunferencia ()