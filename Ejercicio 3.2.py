# Crear una función que calcule el área de un triángulo.
print("Función del área de un triángulo:")

def áreatriángulo ():
    b = int(input("Introduzca el valor de la base: "))
    h = int(input("Introduzca el valor de la altura: "))
    area = (b*h) / 2
    print("El área del triangulo es:", area)

áreatriángulo ()